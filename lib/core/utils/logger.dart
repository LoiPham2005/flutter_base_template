  static void httpResponse(
    String method,
    String url,
    int statusCode, {
    dynamic data,
  }) {
    if (!LogConfig.enableHttpLogs || LogConfig.logOnlyFailedRequests) return;

    final buffer = StringBuffer();
    final uri = Uri.parse(url);
    final statusEmoji = statusCode >= 200 && statusCode < 300 ? '‚úÖ' : '‚ö†Ô∏è';

    buffer.writeln('‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    buffer.writeln('‚ïë $statusEmoji RESPONSE: $statusCode'); 
    buffer.writeln('‚ï†‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    buffer.writeln('‚ïë $method ${uri.path}');

    if (data != null) {
      buffer.writeln('‚ï†‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
      buffer.writeln('‚ïë üì• Response Data:');
      
      // Format data ƒë·ªông theo response
      try {
        final encoder = JsonEncoder.withIndent('  ');
        final formatted = encoder.convert(data)
            .split('\n')
            .map((line) => '‚ïë   $line')
            .join('\n');
        buffer.writeln(formatted);
      } catch (e) {
        // N·∫øu kh√¥ng ph·∫£i JSON th√¨ hi·ªÉn th·ªã tr·ª±c ti·∫øp
        buffer.writeln('‚ïë   $data');
